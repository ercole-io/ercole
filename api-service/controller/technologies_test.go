func TestListTechnologies_Success(t *testing.T) {
	mockCtrl := gomock.NewController(t)
	defer mockCtrl.Finish()
	as := NewMockAPIServiceInterface(mockCtrl)
	ac := APIController{
		TimeNow: utils.Btc(utils.P("2019-11-05T14:02:03Z")),
		Service: as,
		Config:  config.Configuration{},
		Log:     utils.NewLogger("TEST"),
	}

	expectedRes := []model.TechnologyStatus{
		{
			Compliance:          0.0,
			TotalCost:           100,
			PaidCost:            20,
			CoveredByAgreements: 0,
			Product:             model.TechnologyOracleDatabase,
			ConsumedByHosts:     8,
			HostsCount:          10,
		},
		{
			Compliance:          1.0,
			TotalCost:           15,
			PaidCost:            3,
			CoveredByAgreements: 2,
			Product:             model.TechnologyOracleExadata,
			ConsumedByHosts:     2,
			HostsCount:          1,
		},
	}

	as.EXPECT().
		ListManagedTechnologies("Count", true, "Italy", "TST", utils.P("2020-06-10T11:54:59Z")).
		Return(expectedRes, nil)

	rr := httptest.NewRecorder()
	handler := http.HandlerFunc(ac.ListTechnologies)
	req, err := http.NewRequest("GET", "/assets?sort-by=Count&sort-desc=true&location=Italy&environment=TST&older-than=2020-06-10T11%3A54%3A59Z", nil)
	require.NoError(t, err)

	handler.ServeHTTP(rr, req)

	require.Equal(t, http.StatusOK, rr.Code)
	assert.JSONEq(t, utils.ToJSON(expectedRes), rr.Body.String())
}

